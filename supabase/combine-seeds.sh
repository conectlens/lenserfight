#!/usr/bin/env bash
set -euo pipefail
cd "$(dirname "$0")"

SEED_DIR="seeds"
OUTPUT="seed.sql"

if [ ! -d "$SEED_DIR" ]; then
  echo "Error: $SEED_DIR directory not found" >&2
  exit 1
fi

FILES=$(ls "$SEED_DIR"/[0-9][0-9]_*.sql 2>/dev/null || true)

if [ -z "$FILES" ]; then
  echo "Error: No seed files found matching ${SEED_DIR}/[0-9][0-9]_*.sql" >&2
  exit 1
fi

echo "-- ==============================================================================" > "$OUTPUT"
echo "-- LenserFight Combined Seed Data" >> "$OUTPUT"
echo "-- Auto-generated by combine-seeds.sh — DO NOT EDIT DIRECTLY" >> "$OUTPUT"
echo "-- Edit individual files in supabase/seeds/ instead." >> "$OUTPUT"
echo "-- ==============================================================================" >> "$OUTPUT"
echo "" >> "$OUTPUT"

for f in $FILES; do
  echo "" >> "$OUTPUT"
  echo "-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~" >> "$OUTPUT"
  echo "-- Source: $f" >> "$OUTPUT"
  echo "-- ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~" >> "$OUTPUT"
  echo "" >> "$OUTPUT"
  cat "$f" >> "$OUTPUT"
  echo "" >> "$OUTPUT"
done

COUNT=$(echo "$FILES" | wc -l | tr -d ' ')
echo "seed.sql generated from $COUNT seed files"
